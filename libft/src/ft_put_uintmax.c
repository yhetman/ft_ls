#include "libft.h"

void	ft_put_uintmax(uintmax_t n)
{
	if (n >= 10)
	{
		ft_put_uintmax(n / 10);
		ft_putchar(n % 10 + '0');
	}
	if (n < 10)
		ft_putchar(n + '0');
}
